'''
a una persona se le paga 30000 la hora menos 5% de ahorro
cuanto da el porcentaje de ahorro y cuanto se le paga en total? eso es lo que calcula este algoritmo
'''

pago_hora = 30000
ahorro = pago_hora/100*5
total = pago_hora - ahorro

print(f"El ahorro total por hora es: {ahorro}\nEl pago total por hora es: {total}")